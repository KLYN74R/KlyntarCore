    setInterval(()=>{

        getBlockByIndex(btcFork,1000000).then(block=>{

            console.log('Block is ',block)
            console.log('Going to fetch txs')

            block.tx.forEach(async hash=>{

                let tx = await getTransaction(btcFork,hash)

                console.log(tx)

            })

        })

    },3000)

